#Learning to use float and int
flyttall_1=float(input("Skriv inn et flytall:"))
flyttall_2=float(input("Skriv inn enda et flyttall:"))
flyttall_3=float(input("Skriv inn et siste flyttall:"))
print("Konvertert til heltall blir det:",int(flyttall_1), int(flyttall_2), int(flyttall_3))
